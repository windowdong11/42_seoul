#include "utils_3d.h"

t_vector3	*v3_transform(t_vector3 *out,
				const t_vector3 *v, const t_matrix3 *m)
{
	return (vector3(
			out,
			m->_11 * v->x + m->_21 * v->y + m->_31 * v->z,
			m->_12 * v->x + m->_22 * v->y + m->_32 * v->z,
			m->_13 * v->x + m->_23 * v->y + m->_33 * v->z
		));
}
