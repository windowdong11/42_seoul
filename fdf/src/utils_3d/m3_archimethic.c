#include "utils_3d.h"

t_matrix3	*m3_add(t_matrix3 *out, const t_matrix3 *m1, const t_matrix3 *m2)
{
	out->_11 = m1->_11 + m2->_11;
	out->_12 = m1->_12 + m2->_12;
	out->_13 = m1->_13 + m2->_13;
	out->_21 = m1->_21 + m2->_21;
	out->_22 = m1->_22 + m2->_22;
	out->_23 = m1->_23 + m2->_23;
	out->_31 = m1->_31 + m2->_31;
	out->_32 = m1->_32 + m2->_32;
	out->_33 = m1->_33 + m2->_33;
	return (out);
}

t_matrix3	*m3_multiply(t_matrix3 *out,
					const t_matrix3 *m1, const t_matrix3 *m2)
{
	t_matrix3	ret;

	ret._11 = m1->_11 * m2->_11 + m1->_12 * m2->_21 + m1->_13 * m2->_31;
	ret._12 = m1->_11 * m2->_12 + m1->_12 * m2->_22 + m1->_13 * m2->_32;
	ret._13 = m1->_11 * m2->_13 + m1->_12 * m2->_23 + m1->_13 * m2->_33;
	ret._21 = m1->_21 * m2->_11 + m1->_22 * m2->_21 + m1->_23 * m2->_31;
	ret._22 = m1->_21 * m2->_12 + m1->_22 * m2->_22 + m1->_23 * m2->_32;
	ret._23 = m1->_21 * m2->_13 + m1->_22 * m2->_23 + m1->_23 * m2->_33;
	ret._31 = m1->_31 * m2->_11 + m1->_32 * m2->_21 + m1->_33 * m2->_31;
	ret._32 = m1->_31 * m2->_12 + m1->_32 * m2->_22 + m1->_33 * m2->_32;
	ret._33 = m1->_31 * m2->_13 + m1->_32 * m2->_23 + m1->_33 * m2->_33;
	out->_11 = ret._11;
	out->_12 = ret._12;
	out->_13 = ret._13;
	out->_21 = ret._21;
	out->_22 = ret._22;
	out->_23 = ret._23;
	out->_31 = ret._31;
	out->_32 = ret._32;
	out->_33 = ret._33;
	return (out);
}
