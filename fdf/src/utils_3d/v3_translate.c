#include "utils_3d.h"

t_vector3	*v3_translate(t_vector3 *out,
				const t_vector3 *v1, const t_vector3 *v2)
{
	return (vector3(out, v1->x + v2->x, v1->y + v2->y, v1->z + v2->z));
}
